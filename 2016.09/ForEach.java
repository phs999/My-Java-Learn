class ForEach 
{
	/*
	时间：2016.09.11
	作者：phs
	功能：foreach 语句练习
	思路：
	for(int element:numArray)
	{
		System.out.print(element);
	}
	
	
	*/
	public static void main(String[] args) 
	{
		int []numArr = {1,2,3,4,5,6};
		for(int element:numArr)
		{
			System.out.println(element);
		}
		
	}
}
